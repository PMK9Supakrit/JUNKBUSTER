        <div class="transparent">
            <div class="heading">SOLUTIONS</div>
        </div>

        <div class="descript">There are 3 major parts made up of our solution. First, The Base Station. The space junk cleaning system is controlled remotely from this ground-based station by the ground crew with extra longrange radio signals. This mode of control applies mainly for getting the larger pieces of orbital debris. For small and the tiny pieces of debris, the spaceship part would be able to work autonomously to a certain extent using its built-in maneuver algorithm, detecting, and shooting the debris that comes in its range with the laser. It will be more efficient this way since human operators will not be effective at spotting every tiny piece of debris, missing some of them.</div><br>
        <div class="descript">Second is The Space Warehouse, which is located up in the orbit. Debris that could be reused as parts of new space products are collected and temporary stored here. Next, the debris in large size will be dismantled into smaller pieces and loaded onto a space capsule, then transported back to the base station on earth.</div><br>
        <div class="descript">The last and most innovative part is the Junk buster. The Junk Buster is a spaceship which carries out tasks to collect reusable debris and clean up purposeless ones.</div>
    </div>

    <div class="card" style="background-color: #f5b402;">
        <img src="pictures/icon/Spacecraft.png" alt="Spacecraft">

        <div class="transparent">
            <div class="heading">JUNK BUSTER</div>
        </div>

        <div class="descript">The Junk buster’s maneuver system is provided by an electric propulsion system, which work by electrically expelling propellant at high speed to provide a sustainable thrust. They are advantageous over traditional chemical rockets as they are more efficient at saving energy and weight. They typically use much less propellant than chemical rockets to produce the same overall thrust. This benefits in two ways, it makes the ship lighter since the weight per energy ratio doesn’t weigh as much as a typical liquid fuel, also the ship won’t need to be refueled as frequently, extending the longevity and reducing the cost of cleaning debris.</div><br>
        <div class="descript">For the essential cleaning up of the space debris, the first tool we propose is a powerful focused class 4 laser. The laser itself has an output of more than 500 Watts, combined with a compound convex lens which can be used to evaporate away the smaller pieces of debris, likewise, we can push them out of Earth’s orbit by accurately burning away a certain part of the debris and let the rotational force of the Earth guide the debris out of its orbit. The other tool our ship has is a strong aerogel coated titanium net. The net will be ejected out of the spacecraft’s net gun using an electromagnetic discharge to shoot it rapidly and accurately in space. The net will be used to capture the pieces of debris which are large and still can be reused for other space products. The captured debris will be stored inside the spacecraft’s compartment to be compressed and used as an emergency repair material, or transferred to our space ware house for capsulated and sent back to the base station.</div><br>
        <div class="descript">Other unusable debris can be ejected back into the Earth’s atmosphere using an electromagnetic discharge port like our net gun. The debris will be burned up completely in our atmosphere via friction so it poses absolutely no harm to humans or infrastructures.</div>
    </div>

